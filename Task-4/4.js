// Create three variables and assign values
let one = 1;
let two = 2;
let three = 3;

// Calculate the square of each number
let squareOfOne = one * one;
let squareOfTwo = two * two;
let squareOfThree = three * three;

// Calculate the sum of the squares
let sumOfSquares = squareOfOne + squareOfTwo + squareOfThree;

// Print the sum of the squares
console.log(sumOfSquares); // Output: 14